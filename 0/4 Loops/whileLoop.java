package Loops;
public class whileLoop {

    public static void main(String[] args) {

        int counter = 0;

        while (counter < 100){
            System.out.println("Hello World"); //100 times Hello World
            counter++ ;
        }
        System.out.println("Hello World printed 100 times");
        
    }
    
}

