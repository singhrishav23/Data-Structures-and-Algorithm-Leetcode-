package Loops;

public class DoWhileLoop {

    public static void main(String[] args) {

        int counter = 1;

        do {
            System.out.println("Hello World"); // Pehle code chal gaya 
            counter++;
        } while (counter < 0); //then condition check hui
        
    }
    
}


/*

  Syntac : 
  do{
    //do something
  }while (condition);

  do-while me hume phele ek baar statement run kr dete hain phir condition check krte

*/