//4 --> Assignment Operator


public class Asop {

    public static void main(String[] args) {

        // =
        int a = 5; // number assigned
        System.out.println(a); //5

        // +=
        a += 5; // a = a + 5
        System.out.println(a); //10

        // -=
        a -= 5; //a = a - 5
        System.out.println(a); //5

        // *=
        a *= 2; // a = a * 2
        System.out.println(a); //10

        // /=
        a /= 2; // a = a / 2
        System.out.println(a); //5

        // %=
        a %= 5; // a = a % 5
        System.out.println(a); //0

    }
    
}

/*

  Assignment Operator
  {
    = , += , -= , *= , /= , %=
  }

*/
