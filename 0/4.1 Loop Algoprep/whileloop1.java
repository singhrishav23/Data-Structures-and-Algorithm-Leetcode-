//1 --> Print 1 to 5

public class whileloop1 {

    public static void main(String[] args) {

        int num = 1;
        while (num <= 5) {
            System.out.println(num);
            num++ ;
        }
        
    }
    
}


/*

  While Loop Structure/Syntax

  int n;  --> initialize

  while (condition) { --> condition
            --->Statement you want to run
            -->Updation of loop variable
  }
 
*/
